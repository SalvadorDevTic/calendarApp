export const getNewContentCalendarDays = (from, to) => {
  let temp = 0;
  let fromCounter = from;
  const oneDay = 86400000;
  let contents = [];

  while (fromCounter <= to) {
    //obtiene turnos y usuarios.
    let shiftMorning = {
      id: 0,
      tipo: "Mañana",
      users: [],
    };

    let shiftEvening = {
      id: 1,
      tipo: "Tarde",
      users: [],
    };

    let userB = {
      id: 0,
      username: "Marcos",
      email: "marcos@correo.com",
      role: "responsable",
      label: "plantilla",
    };
    let userA = {
      id: 1,
      username: "Maria",
      email: "maria@correo.com",
      role: "responsable",
      label: "plantilla",
    };

    //lista Vacia de turnos para añadir a contentDay
    let shifts = [];

    //Inicia la fecha para añadir al content
    let auxDateTS = new Date(fromCounter);

    //añade la fecha al contentDay
    let content = {
      id: fromCounter,
      date: {
        day: auxDateTS.getDate(),
        month: auxDateTS.getMonth(),
        year: auxDateTS.getFullYear(),
      },
    };

    //aplicar segundo dia, reset contador
    if (temp == 1) {
      shiftMorning.users.push(userB);
      shiftEvening.users.push(userA);
    }
    //aplicar primer dia, aumentar 1 el contador
    if (temp == 0) {
      shiftMorning.users.push(userA);
      shiftEvening.users.push(userB);
    }
    temp == 1 ? (temp = 0) : temp++;
    shifts.push(shiftMorning);
    shifts.push(shiftEvening);
    contents.push({ ...content, shifts: shifts });
    // shiftEvening = { ...shiftEvening, users: [] };
    // shiftMorning = { ...shiftMorning, users: [] };
    fromCounter += oneDay;
  }

  return contents;
};

export const contentCalendarDays = () => {
  return getNewContentCalendarDays(
    new Date().setFullYear(2024, 0, 1),
    new Date().setFullYear(2024, 11, 31)
  );
};
